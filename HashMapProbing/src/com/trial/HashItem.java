package com.trial;

public class HashItem {
	
	private int key;
	private int value;
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public HashItem(int key, int value) {
		super();
		this.key = key;
		this.value = value;
	}
	
	
}
